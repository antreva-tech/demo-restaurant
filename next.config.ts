import type { NextConfig } from "next";

/**
 * Next.js configuration with security headers and image optimisation.
 * Additional CSP / HSTS headers are applied in middleware.ts for dynamic routes.
 */
const nextConfig: NextConfig = {
  images: {
    remotePatterns: [
      {
        protocol: "https",
        hostname: "images.unsplash.com",
        pathname: "/**",
      },
    ],
  },
  /** Security headers applied to all routes (including static assets). */
  async headers() {
    return [
      {
        source: "/(.*)",
        headers: [
          { key: "X-Frame-Options", value: "DENY" },
          { key: "X-Content-Type-Options", value: "nosniff" },
          { key: "Referrer-Policy", value: "strict-origin-when-cross-origin" },
          {
            key: "Permissions-Policy",
            value: "camera=(), microphone=(), geolocation=(), interest-cohort=()",
          },
          {
            key: "Strict-Transport-Security",
            value: "max-age=31536000; includeSubDomains",
          },
        ],
      },
    ];
  },
  /** Disable x-powered-by to reduce fingerprinting surface. */
  poweredByHeader: false,
};

export default nextConfig;
